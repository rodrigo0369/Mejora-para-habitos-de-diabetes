# DiabetesHabitsApp

Una app Flutter diseñada para ayudar a personas con diabetes a mantener hábitos saludables, registrar su glucosa y recibir recordatorios personalizados.

---

## Funcionalidades

- Registro diario de hábitos.
- Recordatorios configurables para registrar glucosa.
- Alerta visual si el nivel de glucosa es muy bajo o muy alto.
- Historial de registros de glucosa con opción para eliminar.
- Configuración de idioma (Español / Inglés).
- Soporte multilenguaje.
- Notificaciones locales diarias.

